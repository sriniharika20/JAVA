class DaysInWeek {
    public static void main(String[] args) {
        String days[] = {"Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"};
        System.out.println("Days in a week:");
        for (int i = 0; i < days.length; i++) {
            System.out.println(days[i]);
        }
    }
}
